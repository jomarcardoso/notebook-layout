import path from 'node:path';
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

const MIME_BY_EXTENSION = new Map([
  ['.png', 'image/png'],
  ['.jpg', 'image/jpeg'],
  ['.jpeg', 'image/jpeg'],
  ['.webp', 'image/webp'],
  ['.gif', 'image/gif'],
  ['.avif', 'image/avif'],
  ['.svg', 'image/svg+xml'],
]);

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, '..');

const imagesDirectory = path.join(projectRoot, 'img');
const outputFile = path.join(
  projectRoot,
  'styles',
  'utils',
  '_texture-bg.scss',
);

const toPosixPath = (value) => value.split(path.sep).join('/');

const normalizeVariantName = (relativeFilePath) => {
  const extension = path.extname(relativeFilePath);
  const fileNameWithoutExtension = relativeFilePath.slice(0, -extension.length);

  const normalized = fileNameWithoutExtension
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase();

  return normalized || 'texture';
};

const findImageFiles = async (directory) => {
  const entries = await readdir(directory, {
    withFileTypes: true,
  });

  const imageFiles = [];

  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    if (entry.name.startsWith('.')) {
      continue;
    }

    const absolutePath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      imageFiles.push(...(await findImageFiles(absolutePath)));
      continue;
    }

    if (!entry.isFile()) {
      continue;
    }

    const extension = path.extname(entry.name).toLowerCase();

    if (!MIME_BY_EXTENSION.has(extension)) {
      continue;
    }

    imageFiles.push(absolutePath);
  }

  return imageFiles;
};

const buildVariantMixin = (variant) =>
  [
    `// Source: img/${variant.relativeFilePath}`,
    `@mixin texture-bg-${variant.name} {`,
    `  --paper-texture-image: url('${variant.dataUri}');`,
    '}',
  ].join('\n');

const buildVariantDispatcher = (variants) => {
  const availableVariants = variants.map(({ name }) => name).join(', ');

  const lines = ['@mixin texture-bg($variant) {'];

  variants.forEach((variant, index) => {
    lines.push(
      `  ${index === 0 ? '@if' : '@else if'} $variant == '${variant.name}' {`,
      `    @include texture-bg-${variant.name};`,
      '  }',
    );
  });

  lines.push(
    '  @else {',
    `    @error 'Unknown texture background variant "#{$variant}". Available variants: ${availableVariants}.';`,
    '  }',
    '}',
  );

  return lines.join('\n');
};

let imagePaths;

try {
  imagePaths = await findImageFiles(imagesDirectory);
} catch (error) {
  if (error?.code === 'ENOENT') {
    throw new Error(
      `Pasta de imagens não encontrada: ${toPosixPath(
        path.relative(projectRoot, imagesDirectory),
      )}`,
    );
  }

  throw error;
}

if (imagePaths.length === 0) {
  throw new Error(
    `Nenhuma imagem suportada encontrada em: ${toPosixPath(
      path.relative(projectRoot, imagesDirectory),
    )}`,
  );
}

const variants = await Promise.all(
  imagePaths.map(async (imagePath) => {
    const extension = path.extname(imagePath).toLowerCase();
    const mimeType = MIME_BY_EXTENSION.get(extension);

    const relativeFilePath = toPosixPath(
      path.relative(imagesDirectory, imagePath),
    );

    const imageBuffer = await readFile(imagePath);

    return {
      name: normalizeVariantName(relativeFilePath),
      relativeFilePath,
      dataUri: `data:${mimeType};base64,${imageBuffer.toString('base64')}`,
    };
  }),
);

variants.sort((a, b) => a.relativeFilePath.localeCompare(b.relativeFilePath));

const variantNames = new Set();

for (const variant of variants) {
  if (variantNames.has(variant.name)) {
    throw new Error(
      `Duas imagens geraram a mesma variante "${variant.name}". Renomeie uma delas em img/.`,
    );
  }

  variantNames.add(variant.name);
}

const outputContent = [
  '// AUTO-GENERATED FILE.',
  '// Generated by scripts/generate-texture-bg.mjs.',
  '// Source directory: img/.',
  '// Do not edit manually.',
  '',
  ...variants.flatMap((variant) => [buildVariantMixin(variant), '']),
  buildVariantDispatcher(variants),
  '',
].join('\n');

await mkdir(path.dirname(outputFile), {
  recursive: true,
});

let currentContent = '';

try {
  currentContent = await readFile(outputFile, 'utf8');
} catch (error) {
  if (error?.code !== 'ENOENT') {
    throw error;
  }
}

if (currentContent === outputContent) {
  console.log(
    `Texture SCSS already up to date: ${toPosixPath(
      path.relative(projectRoot, outputFile),
    )}`,
  );
} else {
  await writeFile(outputFile, outputContent, 'utf8');

  console.log(
    `Texture SCSS generated: ${toPosixPath(
      path.relative(projectRoot, outputFile),
    )}`,
  );
}
